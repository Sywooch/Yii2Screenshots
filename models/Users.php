<?php

namespace app\models;

use Yii;
use yii\web\IdentityInterface;

/**
 * This is the model class for table "user".
 *
 * @property int $id
 * @property string $username
 * @property string $password
 * @property string $email
 * @property string $created_ip
 * @property string $created_date
 * @property string $login_ip
 * @property string $auth_key
 */
class Users extends \yii\db\ActiveRecord implements \yii\web\IdentityInterface
{
    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return 'user';
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            [['username', 'password', 'email'], 'required', 'message'=>'{attribute} darf nicht leer sein'],
            [['username', 'email'], 'unique'],
            [['email'], 'email', 'message'=>'Die E-Mail ist nicht gÃ¼ltig.'],
            [['created_date'], 'safe'],
            [['username', 'password', 'email', 'created_ip', 'login_ip'], 'string', 'max' => 255],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'username' => 'Username',
            'password' => 'Passwort',
            'email' => 'E-Mail',
            'created_ip' => 'IP bei Registration',
            'created_date' => 'Datum der Account-Erstellung',
            'login_ip' => 'IP bei letztem Login',
        ];
    }

    /** INCLUDE USER LOGIN VALIDATION FUNCTIONS**/
    /**
     * @inheritdoc
     */
    public static function findIdentity($id)
    {
        return static::findOne($id);
    }

    /**
     * @inheritdoc
     */
    /* modified */
    public static function findIdentityByAccessToken($token, $type = null)
    {
        return static::findOne(['access_token' => $token]);
    }

    /* removed
